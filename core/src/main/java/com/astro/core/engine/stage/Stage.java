package com.astro.core.engine.stage;

/**
 * Names all game stages. In file GameStage.json should be the same names.
 */
public enum Stage {
    /**
     * Main menu stage.
     */
    MAIN_MENU,

    /**
     * First level in game.
     */
    LEVEL1,

    /**
     * Game configuration.
     */
    SETTINGS,

    /**
     * Game rank
     */
    HIGHSCORE,

}
